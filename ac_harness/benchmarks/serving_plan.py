# This module reads AC-Core outputs / writes observed evidence.
# It must not implement compiler logic.
"""
Serving benchmark plan generator (§9.2, §9.3).

Mirror of kernel_plan but for the seven serving workload categories
defined in §9.2.
"""
from __future__ import annotations

import json
import os
from typing import Any

SERVING_CATEGORIES: tuple[str, ...] = (
    "short_chat",
    "long_chat",
    "rag_long_context",
    "code_agent",
    "decode_heavy",
    "prefill_heavy",
    "batch_offline",
)

# Each workload has a default "request mix" the runner should use.
_DEFAULT_MIX: dict[str, dict[str, Any]] = {
    "short_chat": {"prompt_tokens": 256, "completion_tokens": 256, "qps": 8},
    "long_chat": {"prompt_tokens": 4096, "completion_tokens": 1024, "qps": 4},
    "rag_long_context": {"prompt_tokens": 16384, "completion_tokens": 512, "qps": 1},
    "code_agent": {"prompt_tokens": 8192, "completion_tokens": 2048, "qps": 2},
    "decode_heavy": {"prompt_tokens": 256, "completion_tokens": 2048, "qps": 4},
    "prefill_heavy": {"prompt_tokens": 8192, "completion_tokens": 64, "qps": 4},
    "batch_offline": {"prompt_tokens": 1024, "completion_tokens": 1024, "qps": 64},
}

_DEFAULT_METRICS = [
    "serving_throughput_tps",
    "ttft_ms",
    "tpot_ms",
    "p99_latency_ms",
    "kv_cache_peak_gb",
]


def build_serving_plan(
    *,
    benchmark_type: str,
    candidate_ids: list[str],
    hardware_id: str,
    runtime: str,
    mix: dict[str, Any] | None = None,
    metrics: list[str] | None = None,
    notes: str = "Dry-run plan generated by AC-Harness.",
) -> dict[str, Any]:
    if benchmark_type not in SERVING_CATEGORIES:
        raise ValueError(
            f"Unknown serving benchmark_type {benchmark_type!r}. "
            f"Known: {SERVING_CATEGORIES}"
        )
    return {
        "schema_version": 1,
        "benchmark_type": benchmark_type,
        "candidate_ids": list(candidate_ids),
        "hardware_id": hardware_id,
        "runtime": runtime,
        "mix": dict(mix) if mix is not None else dict(_DEFAULT_MIX[benchmark_type]),
        "metrics": list(metrics) if metrics is not None else list(_DEFAULT_METRICS),
        "notes": notes,
    }


def write_serving_plan(plan: dict[str, Any], output_path: str) -> str:
    os.makedirs(os.path.dirname(os.path.abspath(output_path)) or ".", exist_ok=True)
    with open(output_path, "w") as f:
        json.dump(plan, f, indent=2, sort_keys=True)
    return output_path
