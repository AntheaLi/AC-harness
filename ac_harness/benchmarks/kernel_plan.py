# This module reads AC-Core outputs / writes observed evidence.
# It must not implement compiler logic.
"""
Kernel benchmark plan generator (§9.1, §9.3).

Emits the JSON benchmark plan shape:

    {
      "schema_version": 1,
      "benchmark_type": "decode_kv",
      "candidate_ids": [...],
      "hardware_id": "...",
      "runtime": "...",
      "shapes": [...],
      "metrics": [...],
      "notes": "..."
    }

We DO NOT execute kernels here. We describe what should be measured so
an external runner (or `executor.local`) can carry out the work. Output
files are deterministic JSON suitable for diffing.
"""
from __future__ import annotations

import json
import os
from typing import Any

from .microbench_templates import (
    KERNEL_CATEGORIES,
    default_metrics,
    default_shapes,
)


def build_kernel_plan(
    *,
    benchmark_type: str,
    candidate_ids: list[str],
    hardware_id: str,
    runtime: str,
    shapes: list[dict[str, Any]] | None = None,
    metrics: list[str] | None = None,
    notes: str = "Dry-run plan generated by AC-Harness.",
) -> dict[str, Any]:
    if benchmark_type not in KERNEL_CATEGORIES:
        raise ValueError(
            f"Unknown kernel benchmark_type {benchmark_type!r}. "
            f"Known: {KERNEL_CATEGORIES}"
        )
    return {
        "schema_version": 1,
        "benchmark_type": benchmark_type,
        "candidate_ids": list(candidate_ids),
        "hardware_id": hardware_id,
        "runtime": runtime,
        "shapes": list(shapes) if shapes is not None else default_shapes(benchmark_type),
        "metrics": list(metrics) if metrics is not None else default_metrics(benchmark_type),
        "notes": notes,
    }


def write_kernel_plan(plan: dict[str, Any], output_path: str) -> str:
    os.makedirs(os.path.dirname(os.path.abspath(output_path)) or ".", exist_ok=True)
    with open(output_path, "w") as f:
        json.dump(plan, f, indent=2, sort_keys=True)
    return output_path
